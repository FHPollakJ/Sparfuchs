package com.sparfuchs.user;

import com.sparfuchs.purchase.Purchase;
import com.sparfuchs.purchaseProduct.PurchaseProduct;
import jakarta.persistence.*;

import java.util.List;

@Entity
@Table(name = "users")
public class User {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(unique = true, nullable = false)
    private String email;

    @Column(nullable = false)
    private String username;

    @Column(nullable = false)
    private String password;

    @OneToMany(mappedBy = "user", cascade = CascadeType.ALL)
    private List<Purchase> purchases;

    private double totalAmountSpent = 0.0;
    private double totalAmountSaved = 0.0;

    public User() {

    }

    public User(String username, String email, String password) {
        this.username = username;
        this.email = email;
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String name) {
        this.username = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public List<Purchase> getPurchases() {
        return purchases;
    }

    public void setPurchases(List<Purchase> purchases) {
        this.purchases = purchases;
    }

    public void updateTotals() {
        this.totalAmountSpent = purchases.stream()
                .mapToDouble(Purchase::getTotalSpent)
                .sum();
        this.totalAmountSaved = purchases.stream()
                .mapToDouble(Purchase::getTotalSaved)
                .sum();
    }
}

